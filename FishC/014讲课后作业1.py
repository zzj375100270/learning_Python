x = int(input("请输入今年的利润"))

if 0 <= x <= 100000:
    y = 0.1 * x

elif 100000 < x <= 200000:
    y = (x - 100000)*0.075 + 10000

elif 200000 < x <= 400000:
    y = (x - 200000)*0.05 + 17500

elif 400000 < x <= 600000:
    y = (x - 400000)*0.03 + 27500

elif 600000 < x <= 1000000:
    y = (x - 600000)*0.015 + 33500

elif x > 1000000:
    y = (x - 1000000)*0.01 + 39500

else:
    print("输入错误！")

print(f"今年的利润是：{x}，应该发放的奖金总数是{y:.1f}！")
